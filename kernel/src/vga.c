static void write_regs(uint8_t *regs)
{
    /* write MISCELLANEOUS reg */
    out_byte(VGA_MISC_WRITE, *regs);
    regs++;
    /* write SEQUENCER regs */
    for(int i = 0; i < VGA_NUM_SEQ_REGS; i++)
    {
        out_byte(VGA_SEQ_INDEX, i);
        out_byte(VGA_SEQ_DATA, *regs);
        regs++;
    }
    /* unlock CRTC registers */
    out_byte(VGA_CRTC_INDEX, 0x03);
    out_byte(VGA_CRTC_DATA, in_byte(VGA_CRTC_DATA) | 0x80);
    out_byte(VGA_CRTC_INDEX, 0x11);
    out_byte(VGA_CRTC_DATA, in_byte(VGA_CRTC_DATA) & ~0x80);
    /* make sure they remain unlocked */
    regs[0x03] |= 0x80;
    regs[0x11] &= ~0x80;
    /* write CRTC regs */
    for(int i = 0; i < VGA_NUM_CRTC_REGS; i++)
    {
        out_byte(VGA_CRTC_INDEX, i);
        out_byte(VGA_CRTC_DATA, *regs);
        regs++;
    }
    /* write GRAPHICS CONTROLLER regs */
    for(int i = 0; i < VGA_NUM_GC_REGS; i++)
    {
        out_byte(VGA_GC_INDEX, i);
        out_byte(VGA_GC_DATA, *regs);
        regs++;
    }
    /* write ATTRIBUTE CONTROLLER regs */
    for(int i = 0; i < VGA_NUM_AC_REGS; i++)
    {
        (void)in_byte(VGA_INSTAT_READ);
        out_byte(VGA_AC_INDEX, i);
        out_byte(VGA_AC_WRITE, *regs);
        regs++;
    }
    /* lock 16-color palette and unblank display */
    (void)in_byte(VGA_INSTAT_READ);
    out_byte(VGA_AC_INDEX, 0x20);
}

static void set_plane(uint32_t pane)
{
    uint8_t pane_mask;

    assert((pane & ~3) == 0 && "Invalid pane");
    pane_mask = 1 << pane;
/* set read plane */
    out_byte(VGA_GC_INDEX, 4);
    out_byte(VGA_GC_DATA, pane);
/* set write plane */
    out_byte(VGA_SEQ_INDEX, 2);
    out_byte(VGA_SEQ_DATA, pane_mask);
}

/**
 * @brief VGA framebuffer is at 0xA0000, 0xB0000, or 0xB8000 depending on bits in GC 6
 */
static uint8_t *vga_get_framebuffer()
{
    out_byte(VGA_GC_INDEX, 6);
    uint32_t seg = in_byte(VGA_GC_DATA);
    seg >>= 2;
    seg &= 3;
    switch (seg)
    {
        default:
        case 0:
        case 1:
            return (uint8_t *)0xA0000;
        case 2:
            return (uint8_t *)0xB0000;
        case 3:
            return (uint8_t *)0xB8000;
    }

    assert(false && "Unexpected GC value from VGA");
}
static void write_font(uint8_t *font_buf)
{
    uint8_t seq2, seq4, gc4, gc5, gc6;

    /* save registers
set_plane() modifies GC 4 and SEQ 2, so save them as well */
    out_byte(VGA_SEQ_INDEX, 2);
    seq2 = in_byte(VGA_SEQ_DATA);

    out_byte(VGA_SEQ_INDEX, 4);
    seq4 = in_byte(VGA_SEQ_DATA);
    /* turn off even-odd addressing (set flat addressing)
assume: chain-4 addressing already off */
    out_byte(VGA_SEQ_DATA, seq4 | 0x04);

    out_byte(VGA_GC_INDEX, 4);
    gc4 = in_byte(VGA_GC_DATA);

    out_byte(VGA_GC_INDEX, 5);
    gc5 = in_byte(VGA_GC_DATA);
    /* turn off even-odd addressing */
    out_byte(VGA_GC_DATA, gc5 & ~0x10);

    out_byte(VGA_GC_INDEX, 6);
    gc6 = in_byte(VGA_GC_DATA);
    /* turn off even-odd addressing */
    out_byte(VGA_GC_DATA, gc6 & ~0x02);
    /* write font to plane P4 */
    set_plane(2);
    /* write font 0 (to write font 1, offset the framebuffer by 0x4000)*/
	for(int i = 0; i < 256; i++)
	{
		memmove(vga_get_framebuffer() + i * 32, font_buf, VGA_FONT_HEIGHT);
		font_buf += VGA_FONT_HEIGHT;
	}
    /* restore registers */
    out_byte(VGA_SEQ_INDEX, 2);
    out_byte(VGA_SEQ_DATA, seq2);
    out_byte(VGA_SEQ_INDEX, 4);
    out_byte(VGA_SEQ_DATA, seq4);
    out_byte(VGA_GC_INDEX, 4);
    out_byte(VGA_GC_DATA, gc4);
    out_byte(VGA_GC_INDEX, 5);
    out_byte(VGA_GC_DATA, gc5);
    out_byte(VGA_GC_INDEX, 6);
    out_byte(VGA_GC_DATA, gc6);
}

void vga_color_index(uint8_t color_index)
{
    out_byte(VGA_DAC_MASK, 0xff);               // Mask all register so we could update any of them
    out_byte(VGA_DAC_WRITE_INDEX, color_index); // Set the index
}

void vga_color_write_color(uint8_t red, uint8_t green, uint8_t blue)
{
    out_byte(VGA_DAC_DATA, red >> 2);
    out_byte(VGA_DAC_DATA, green >> 2);
    out_byte(VGA_DAC_DATA, blue >> 2);
}

void vga_color_palette(uint8_t color_index, const uint8_t *palette, uint32_t palette_length)
{
    vga_color_index(color_index);

    for (int i = 0; i < palette_length * 3; i++)
    {
        out_byte(VGA_DAC_DATA, palette[i] >> 2);
    }
}

void vga_color(uint8_t color_index, uint8_t red, uint8_t green, uint8_t blue)
{
    vga_color_index(color_index);

    vga_color_write_color(red, green, blue);
}

void vga_mode_graphics()
{
    write_regs(g_320x200x256);
}

void vga_mode_text()
{
    write_regs(g_80x25_text);
    write_font(g_8x16_font);
}

